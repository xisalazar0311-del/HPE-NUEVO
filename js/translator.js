/* ─────────────────────────────────────────────────────────
   Motor de traducción
───────────────────────────────────────────────────────── */
let currentLang = localStorage.getItem('sitelang') || 'es';

function applyTranslation(lang) {
  document.documentElement.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[key] && translations[key][lang] !== undefined) {
      el.textContent = translations[key][lang];
    }
  });

  const btn = document.getElementById('lang-toggle-btn');
  if (btn && translations['lang.btn.title']) {
    btn.title = translations['lang.btn.title'][lang];
  }

  localStorage.setItem('sitelang', lang);
  currentLang = lang;
}

function toggleLanguage() {
  const newLang = currentLang === 'es' ? 'en' : 'es';
  applyTranslation(newLang);

  const btn = document.getElementById('lang-toggle-btn');
  if (btn) {
    btn.classList.add('lang-btn--spinning');
    setTimeout(() => btn.classList.remove('lang-btn--spinning'), 300);
  }
}

document.addEventListener('DOMContentLoaded', () => {
  applyTranslation(currentLang);
  const btn = document.getElementById('lang-toggle-btn');
  if (btn) btn.addEventListener('click', toggleLanguage);
});